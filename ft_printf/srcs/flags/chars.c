#include <unistd.h>
#include "../../includes/core.h"

int	flag_c(va_list args, t_modifiers modifiers)
{
	int c;

	(void)modifiers;
	c = va_arg(args, int);
	write(STDOUT, &c, 1);
	return (1);
}
